/* Declaración y Asignación de Variables:
1. Declara y asigna un valor numérico a dos variables: primerNumero y
segundoNumero.*/

let primerNumero = 100;
let segundoNumero = 25;

/*Operaciones Matemáticas:
2. Declara las siguientes variables: resultadoSuma, resultadoResta,
resultadoMultiplicacion, resultadoDivision.*/
let resultadoSuma;
let resultadoResta;
let resultadoMultiplicacion;
let resultadoDivision;

/*3. Asigna a cada una de estas variables el resultado de la suma, resta,
multiplicación y división de las variables primerNumero y
segundoNumero.*/
resultadoSuma = primerNumero + segundoNumero;
resultadoResta = primerNumero - segundoNumero;
resultadoMultiplicacion = primerNumero * segundoNumero;
resultadoDivision = primerNumero / segundoNumero;

/*Mostrar Resultados:
1. Utiliza console.log(nombreDeLaVariable) para imprimir cada uno de
los resultados y ver cómo se ven los mismos. */

console.log(
  `El resultado de la suma entre ${primerNumero} y ${segundoNumero} es: ${resultadoSuma}`
);
console.log(
  `El resultado de la resta entre ${primerNumero} y ${segundoNumero} es: ${resultadoResta}`
);
console.log(
  `El resultado de la multiplicación entre ${primerNumero} y ${segundoNumero} es: ${resultadoMultiplicacion}`
);
console.log(
  `El resultado de la división entre ${primerNumero} y ${segundoNumero} es: ${resultadoDivision}`
);
