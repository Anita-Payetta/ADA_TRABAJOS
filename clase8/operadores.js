//Operadores aritméticos

let a = 10;
let b = 3;

console.log(a + b); //Suma: 13
console.log(a - b); //Resta: 7
console.log(a * b); //Multiplicación: 30
console.log(a / b); //División: 3.333333333
console.log(a % b); //Resto: 1

//Operadores de comparación
console.log(a > b); //true
console.log(a < b); //false
console.log(a === "10"); //false a no es de tipo string

//Operadores lógicos
console.log(true && false); //false  && (AND): me devuelve true sólo si ambos valores son true
